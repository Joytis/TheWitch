#!/usr/bin/env node
/*
 * compare.js — pool-makeup + efficiency comparison: Witch vs the five base classes.
 *
 * Reads Docs/card-data/{cards,silent,necrobinder,ironclad,defect,regent}.json and writes
 * Docs/pool-comparison.md. Pure derivation — no curated state lives here; safe to re-run
 * any time (regen.js chains it after the art tracker).
 *
 * Method notes:
 * - Witch Token/Status-rarity cards are excluded from distributions/efficiency and reported
 *   separately (base classes have no token layer in their pools).
 * - Damage/block stats use only the FLAT bucket. Scaling ("for each"), X-cost, and
 *   conditional-hit cards are counted per class, not averaged — a mean over them is meaningless.
 * - Effective damage = per-hit damage × parsed hit count ("N times"/"twice" in the same
 *   sentence). AoE (ALL enemies) is kept in a separate cell from single-target.
 * - Regent cards with a star cost (sub: Stars:Spend) are excluded from baseline medians:
 *   they are subsidized by a second currency and read as above-rate otherwise.
 * - Baseline = the five base classes pooled, per energy-cost cell; medians are robust to the
 *   handful of rider-heavy cards in each cell.
 */
const fs = require("fs");
const path = require("path");

const HERE = __dirname;
const OUT = path.join(HERE, "..", "pool-comparison.md");

const CLASSES = [
  ["witch", "cards"],
  ["silent", "silent"],
  ["necrobinder", "necrobinder"],
  ["ironclad", "ironclad"],
  ["defect", "defect"],
  ["regent", "regent"],
];
const BASE = ["silent", "necrobinder", "ironclad", "defect", "regent"];

const pools = {};
for (const [cls, file] of CLASSES) {
  pools[cls] = JSON.parse(fs.readFileSync(path.join(HERE, file + ".json"), "utf8")).cards;
}

// ---------- classification ----------

const isToken = (c) => c.rarity === "Token" || c.rarity === "Status";
const mainPool = (cls) => (cls === "witch" ? pools.witch.filter((c) => !isToken(c)) : pools[cls]);
const starCost = (c) => (c.sub || []).includes("Stars:Spend");

function sentences(text) {
  return (text || "").replace(/\n/g, " ").split(/(?<=\.)\s+/);
}

// Parse the damage line of a card into {bucket, perHit, hits, aoe} (or null if no damage line).
// bucket: flat | scaling | xcost | conditional | engine
function dmgInfo(c) {
  if (!c.numbers || !("Damage" in c.numbers)) return null;
  const s = sentences(c.text).find((s) => /deals? .*?damage/i.test(s));
  if (!s) return null;
  const aoe = /ALL enem/i.test(s);
  if (c.cost === "X" || /\bX times\b/.test(s)) return { bucket: "xcost", aoe };
  if (/for each|equal to|CalculatedHits/i.test(s)) return { bucket: "scaling", aoe };
  if (/start of|end of|whenever|next turn/i.test(s)) return { bucket: "engine", aoe };
  let hits = 1;
  const m = s.match(/(\d+) times/);
  if (m) hits = parseInt(m[1], 10);
  else if (/twice/i.test(s)) hits = 2;
  // "If ... hits twice" / "Hits an additional time" = conditional bonus; base stays 1 hit
  const conditional = /if |additional time/i.test(s) && hits === 1;
  return { bucket: conditional ? "conditional" : "flat", perHit: c.numbers.Damage, hits, aoe };
}

// Same for block: {bucket, amount}
function blockInfo(c) {
  if (!c.numbers || !("Block" in c.numbers)) return null;
  const s = sentences(c.text).find((s) => /gains? .*?block|block equal/i.test(s));
  if (!s) return null;
  if (c.cost === "X") return { bucket: "xcost" };
  if (/for each|equal to/i.test(s)) return { bucket: "scaling" };
  if (/start of|end of|whenever|next turn|each turn/i.test(s)) return { bucket: "engine" };
  let amount = c.numbers.Block;
  const m = s.match(/(\d+) times/);
  if (m) amount *= parseInt(m[1], 10);
  else if (/twice/i.test(s)) amount *= 2;
  return { bucket: "flat", amount };
}

function upgradeDelta(c, key) {
  const m = (c.upgrade || "").match(new RegExp(key + " \\+(\\d+)"));
  return m ? parseInt(m[1], 10) : 0;
}

const median = (a) => {
  if (!a.length) return null;
  const s = [...a].sort((x, y) => x - y);
  return s.length % 2 ? s[(s.length - 1) / 2] : (s[s.length / 2 - 1] + s[s.length / 2]) / 2;
};
const fmt = (n) => (n === null ? "—" : Number.isInteger(n) ? String(n) : n.toFixed(1));

// ---------- report ----------

const L = [];
const p = (s = "") => L.push(s);

p("# Pool Comparison — Witch vs Base Classes");
p();
p(`> Generated by \`Docs/card-data/compare.js\` on ${new Date().toISOString().slice(0, 10)} — do not hand-edit.`);
p(`> Method + caveats at the bottom. Witch pool = ${mainPool("witch").length} cards (tokens/status excluded; ${pools.witch.length - mainPool("witch").length} tokens reported separately).`);
p();

// --- 1. distributions ---
p("## 1. Distributions");
p();
const RARITIES = ["Starter", "Common", "Uncommon", "Rare", "Ancient"];
const TYPES = ["Attack", "Skill", "Power"];
const COSTS = ["0", "1", "2", "3", "4+", "X"];
const costKey = (c) => (c.cost === "X" ? "X" : c.cost >= 4 ? "4+" : String(c.cost));

function distTable(title, keys, keyOf) {
  p(`### ${title}`);
  p();
  p("| | " + CLASSES.map(([c]) => c).join(" | ") + " |");
  p("|---|" + CLASSES.map(() => "---").join("|") + "|");
  for (const k of keys) {
    p(`| ${k} | ` + CLASSES.map(([cls]) => mainPool(cls).filter((c) => keyOf(c) === k).length).join(" | ") + " |");
  }
  p();
}
distTable("Rarity", RARITIES, (c) => c.rarity);
distTable("Card type", TYPES, (c) => c.type);
distTable("Energy cost", COSTS, costKey);

// --- 2. damage ---
p("## 2. Damage efficiency (flat bucket only)");
p();
p("Effective damage = per-hit × hits. Cells: energy cost × single-target/AoE. Baseline = 5 base classes pooled (Regent star-cost cards excluded).");
p();

function dmgCards(cls) {
  return mainPool(cls)
    .map((c) => ({ c, d: dmgInfo(c) }))
    .filter((x) => x.d);
}
const baseDmg = BASE.flatMap((cls) => dmgCards(cls).filter((x) => !(cls === "regent" && starCost(x.c))));

p("| cost | shape | base n | base med | base med+ | witch n | witch med | witch med+ |");
p("|---|---|---|---|---|---|---|---|");
for (const cost of ["0", "1", "2", "3"]) {
  for (const aoe of [false, true]) {
    const sel = (arr) => arr.filter((x) => x.d.bucket === "flat" && costKey(x.c) === cost && x.d.aoe === aoe);
    const eff = (x) => x.d.perHit * x.d.hits;
    const effUp = (x) => (x.d.perHit + upgradeDelta(x.c, "Damage")) * x.d.hits;
    const b = sel(baseDmg), w = sel(dmgCards("witch"));
    if (!b.length && !w.length) continue;
    p(`| ${cost} | ${aoe ? "AoE" : "single"} | ${b.length} | ${fmt(median(b.map(eff)))} | ${fmt(median(b.map(effUp)))} | ${w.length} | ${fmt(median(w.map(eff)))} | ${fmt(median(w.map(effUp)))} |`);
  }
}
p();

p("Non-flat damage cards per class (not averaged — counted):");
p();
p("| bucket | " + CLASSES.map(([c]) => c).join(" | ") + " |");
p("|---|" + CLASSES.map(() => "---").join("|") + "|");
for (const bucket of ["scaling", "xcost", "conditional", "engine"]) {
  p(`| ${bucket} | ` + CLASSES.map(([cls]) => dmgCards(cls).filter((x) => x.d.bucket === bucket).length).join(" | ") + " |");
}
p();

// --- 3. block ---
p("## 3. Block efficiency (flat bucket only)");
p();
function blkCards(cls) {
  return mainPool(cls)
    .map((c) => ({ c, b: blockInfo(c) }))
    .filter((x) => x.b);
}
const baseBlk = BASE.flatMap((cls) => blkCards(cls).filter((x) => !(cls === "regent" && starCost(x.c))));

p("| cost | base n | base med | base med+ | witch n | witch med | witch med+ |");
p("|---|---|---|---|---|---|---|");
for (const cost of ["0", "1", "2", "3"]) {
  const sel = (arr) => arr.filter((x) => x.b.bucket === "flat" && costKey(x.c) === cost);
  const eff = (x) => x.b.amount;
  const effUp = (x) => x.b.amount + upgradeDelta(x.c, "Block");
  const b = sel(baseBlk), w = sel(blkCards("witch"));
  if (!b.length && !w.length) continue;
  p(`| ${cost} | ${b.length} | ${fmt(median(b.map(eff)))} | ${fmt(median(b.map(effUp)))} | ${w.length} | ${fmt(median(w.map(eff)))} | ${fmt(median(w.map(effUp)))} |`);
}
p();
p("Non-flat block cards per class:");
p();
p("| bucket | " + CLASSES.map(([c]) => c).join(" | ") + " |");
p("|---|" + CLASSES.map(() => "---").join("|") + "|");
for (const bucket of ["scaling", "xcost", "engine"]) {
  p(`| ${bucket} | ` + CLASSES.map(([cls]) => blkCards(cls).filter((x) => x.b.bucket === bucket).length).join(" | ") + " |");
}
p();

// --- 4. witch outliers ---
p("## 4. Witch outliers vs baseline cell median (±30%)");
p();
const rows = [];
const MIN_CELL = 4; // a median over fewer baseline cards is noise, not a benchmark
for (const x of dmgCards("witch").filter((x) => x.d.bucket === "flat")) {
  const cell = baseDmg.filter((y) => y.d.bucket === "flat" && costKey(y.c) === costKey(x.c) && y.d.aoe === x.d.aoe);
  if (cell.length < MIN_CELL) continue;
  const m = median(cell.map((y) => y.d.perHit * y.d.hits));
  if (m === null) continue;
  const eff = x.d.perHit * x.d.hits;
  const ratio = eff / m;
  if (ratio > 1.3 || ratio < 0.7) {
    const kw = (x.c.threads || []).filter((t) => ["SelfExhaust", "Drawback", "HPLoss", "SelfDebuff", "ExhaustOutlet", "DelayedEffect"].includes(t));
    rows.push(`| ${x.c.name} | dmg | ${costKey(x.c)}${x.d.aoe ? " AoE" : ""} | ${eff} | ${fmt(m)} | ${ratio.toFixed(2)} | ${kw.join(", ")} |`);
  }
}
for (const x of blkCards("witch").filter((x) => x.b.bucket === "flat")) {
  const cell = baseBlk.filter((y) => y.b.bucket === "flat" && costKey(y.c) === costKey(x.c));
  if (cell.length < MIN_CELL) continue;
  const m = median(cell.map((y) => y.b.amount));
  if (m === null) continue;
  const ratio = x.b.amount / m;
  if (ratio > 1.3 || ratio < 0.7) {
    const kw = (x.c.threads || []).filter((t) => ["SelfExhaust", "Drawback", "HPLoss", "SelfDebuff", "ExhaustOutlet", "DelayedEffect"].includes(t));
    rows.push(`| ${x.c.name} | block | ${costKey(x.c)} | ${x.b.amount} | ${fmt(m)} | ${ratio.toFixed(2)} | ${kw.join(", ")} |`);
  }
}
if (rows.length) {
  p("| card | metric | cell | value | base med | ratio | priced-in downsides |");
  p("|---|---|---|---|---|---|---|");
  rows.forEach((r) => p(r));
  p();
  p("A ratio far from 1.0 is a *scrutiny flag*, not a verdict — riders (debuffs, draw, brews) justify low ratios; downsides (exhaust, HP) justify high ones.");
} else {
  p("None — every flat Witch damage/block card sits within ±30% of its baseline cell median.");
}
p();

// --- 5. interplay ---
p("## 5. Pillar interplay (mechanics × role)");
p();
for (const [cls] of CLASSES) {
  const cards = cls === "witch" ? pools.witch : pools[cls]; // tokens count for witch here (own column)
  const mechs = [...new Set(cards.flatMap((c) => c.mechanics || []))].filter((m) => m !== "None");
  p(`### ${cls}`);
  p();
  p("| mechanic | Gen | Payoff | Enabler | Token | gen:payoff |");
  p("|---|---|---|---|---|---|");
  for (const m of mechs) {
    const of = (role) => cards.filter((c) => (c.mechanics || []).includes(m) && (c.role || []).includes(role)).length;
    const g = of("Generator"), pay = of("Payoff");
    p(`| ${m} | ${g} | ${pay} | ${of("Enabler")} | ${of("Token")} | ${pay ? (g / pay).toFixed(1) : "∞"}:1 |`);
  }
  p();
}

// --- 6. thread coverage ---
p("## 6. Cross-class thread coverage");
p();
const allThreads = [...new Set(CLASSES.flatMap(([cls]) => pools[cls].flatMap((c) => c.threads || [])))];
const count = (cls, t) => pools[cls].filter((c) => (c.threads || []).includes(t)).length;
const rowsT = allThreads
  .map((t) => ({ t, w: count("witch", t), base: BASE.map((b) => count(b, t)) }))
  .sort((a, b) => b.w + median(b.base) - (a.w + median(a.base)));
p("| thread | witch | " + BASE.join(" | ") + " | base med |");
p("|---|---|" + BASE.map(() => "---").join("|") + "|---|");
for (const r of rowsT) p(`| ${r.t} | ${r.w} | ${r.base.join(" | ")} | ${fmt(median(r.base))} |`);
p();
const gaps = rowsT.filter((r) => r.w === 0 && median(r.base) >= 2).map((r) => r.t);
const spikes = rowsT.filter((r) => r.w >= 2 * Math.max(...r.base, 1) && r.w >= 4).map((r) => r.t);
if (gaps.length) p(`**Witch gaps** (0 cards where base classes typically have ≥2): ${gaps.join(", ")}.`);
if (spikes.length) p(`**Witch spikes** (≥2× any base class): ${spikes.join(", ")}.`);
p();

// --- 7. tokens ---
const tokens = pools.witch.filter(isToken);
p("## 7. Witch token layer (excluded above)");
p();
p(`${tokens.length} cards: ${tokens.map((c) => c.name).join(", ")}.`);
p();

// --- caveats ---
p("## Method & caveats");
p();
p("- Static numbers catch **outliers and shape mismatches**, not true balance: synergy, energy cheat, upgrade access, and potion output (a Witch identity!) are invisible here.");
p("- Flat-bucket medians only; scaling/X/conditional cards are counted, never averaged.");
p("- Base-game JSON text omits keywords (Sly/Exhaust/Innate/Retain/star costs) — see `taxonomy.md` data-quality notes; `threads[]` carries the ground truth used for the downside column.");
p("- Multi-hit parsed from card text (\"N times\"/\"twice\", same sentence as the damage clause). Conditional bonus hits count as 1 hit.");
p("- Witch damage also arrives via potions and familiars (tokens) — cross-check §7 and the potion system before reading §2 as the whole damage story.");
p();

fs.writeFileSync(OUT, L.join("\n") + "\n");
console.log(`Wrote ${path.relative(process.cwd(), OUT)} (${L.length} lines)`);

// sanity: list damage-text cards the parser could not classify (has Damage number but no damage sentence)
for (const [cls] of CLASSES) {
  const missed = mainPool(cls).filter((c) => c.numbers && "Damage" in c.numbers && !dmgInfo(c));
  if (missed.length) console.log(`  note ${cls}: Damage number but no parsed damage sentence: ${missed.map((c) => c.entry).join(", ")}`);
}
